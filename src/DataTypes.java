public class DataTypes {

    public static void main(String[] args) {
        int number1 = 5; //Integer
        System.out.println("Integer = " + number1);

        boolean isTrue = true; //boolean
        System.out.println("boolean = " + isTrue);

        char character = 'a';
        System.out.println("character = " + character);

        short number2 = 2;
        System.out.println("short = " + number2);

        long number3 = 10;
        System.out.println("long = " + number3);

        float number4 = (float) 10.1;
        System.out.println("float = " + number4);

        double number5 = 10.1;
        System.out.println("double = " + number5);

        String name = "Tarun";
        System.out.println("String = " + name);

        String[] array = {"Tarun", "Varun"};
    }
}
